package org.mitre.jawb.gui;

import java.io.File;
import javax.swing.filechooser.FileFilter;

/**
 * Easy to create file filtering.
 */
public class DefaultFileFilter extends FileFilter {
  private String[] exts;
  private String desc;

  /**
   * Create a File Filter which accepts on directories and files which end
   * in any of the specified extensions. Extensions may or may not have a
   * one or more '.' specified, but if it is not specified, it will not be
   * required. The pattern matching is <b>not</b> case-sensitive.
   * 
   * @param extensions non-null array of strings, each must have length > 0.
   * @param description short description of files allowed
   */
  public DefaultFileFilter (String[] extensions, String description) {
    // sanity check while we duplicate
    exts = new String[extensions.length];
    for (int i=0; i<extensions.length; i++) {
      if (extensions[i] == null || extensions[i].length() < 1) {
        throw new IllegalArgumentException ("ext "+i+" invalid: "+
                                            extensions[i]);
      } else {
        exts[i] = extensions [i].toLowerCase ();
      }
    }
    desc = description;
  }
  /** The description of this filter. */
  public String getDescription () {
    return desc;
  }
    
  /** Whether the given file is accepted by this filter. */
  public boolean accept (File file) {
    if (file.isDirectory() )
      return true;
      
    String s = file.getName ().toLowerCase ();
    for (int i=0; i<exts.length; i++) {
      if (s.endsWith (exts[i]))
        return true;
    }
    return false;
  }
}

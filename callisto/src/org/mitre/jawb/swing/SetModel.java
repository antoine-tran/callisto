package org.mitre.jawb.swing;

import java.util.Iterator;
import java.util.Set;

import org.mitre.jawb.swing.event.SetDataEvent;
import org.mitre.jawb.swing.event.SetDataListener;

/**
 * This interface defines the methods components use to track changes to an
 * arbitrary Set of objects. Logically the model is a just a Set which may or
 * may not be ordered. Any change to the contents or size of the model must be
 * reported to all the SetDataListeners. Implementations which implement this
 * interface are required to fire the appropriate events whenever the Set itself
 * is modified.
 */
public interface SetModel extends Set {

  /**
   * Adds a listener to the set that's notified each time a change to the data
   * data model occurs.
   */
  public void addSetDataListener (SetDataListener l);

  /**
   * Removes a listener from the list that is notified each time a change to
   * the list data model occurs.
   */
  public void removeSetDataListener (SetDataListener l);
}
